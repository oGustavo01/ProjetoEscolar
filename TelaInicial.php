				<h3>Cabelo blindado ou Corte Dimil</h3>
				<p>
				Criado na periferia de São Paulo por Ariel Franco, o cabelo blindado é um penteado com topete de dar inveja a Elvis Presley, bem no estilo Pompadour. O grande diferencial está em como o penteado se mantém resistente aos testes mais malucos possíveis.

				“O nome blindado é perfeito porque, como é resistente, você pode colocar até uma moto em cima do cabelo que não acontece nada”, explica o paulistano conhecido como Ariel Barbeiro.
				</p>
